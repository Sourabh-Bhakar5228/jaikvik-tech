<script>
    // Hamburger Toggle 
    const showMenu = (toggleId, navId) => {
        const toggle = document.getElementById(toggleId),
            nav = document.getElementById(navId)

        toggle.addEventListener('click', () => {
            nav.classList.toggle('sideMenu')
            toggle.classList.toggle('openMenu')
        })
    }
    showMenu('nav-toggle', 'nav-menu')
</script>

<script>
    // Scroll-Y Navbar Fixed 
    window.onscroll = function() {
        const header = document.getElementById("header");
        if (document.body.scrollTop > 150 || document.documentElement.scrollTop > 150) {
            header.classList.add("scrolled");
        } else {
            header.classList.remove("scrolled");
        }
    };
</script>

<script>
    // Drop Down & Nesting Drop Dwon 
    const initDropdown = () => {
        const buttons = document.querySelectorAll('.dropIcon');
        const submenus = document.querySelectorAll('.subDrop');
        const toggleButtons = document.querySelectorAll('.nestingIcon');
        const productLists = document.querySelectorAll('.subNesting');

        const isMobileView = () => window.innerWidth <= 992;

        buttons.forEach((button, index) => {
            button.addEventListener('click', (event) => {
                event.stopPropagation();

                const submenu = submenus[index];

                if (isMobileView()) {

                    submenus.forEach((otherMenu, i) => {
                        if (i !== index) {
                            otherMenu.style.height = '0';
                            buttons[i].classList.remove('active');
                        }
                    });

                    if (submenu.style.height === '0px' || submenu.style.height === '') {
                        submenu.style.height = `${submenu.scrollHeight}px`;
                        button.classList.add('active');
                    } else {
                        submenu.style.height = '0';
                        button.classList.remove('active');
                    }
                } else {

                    buttons.forEach(btn => btn.classList.remove('active'));
                    submenus.forEach(menu => (menu.style.height = ''));
                }
            });
        });

        toggleButtons.forEach((toggleButton, index) => {
            toggleButton.addEventListener('click', (event) => {
                event.stopPropagation();

                if (isMobileView()) {
                    const productList = productLists[index];
                    const parentMenu = submenus[index];

                    if (productList.style.maxHeight === '0px' || !productList.style.maxHeight) {
                        productList.style.maxHeight = `${productList.scrollHeight}px`;
                        toggleButton.classList.add('active');

                        parentMenu.style.height =
                            `${parentMenu.scrollHeight + productList.scrollHeight}px`;
                    } else {
                        productList.style.maxHeight = '0';
                        toggleButton.classList.remove('active');

                        parentMenu.style.height =
                            `${parentMenu.scrollHeight - productList.scrollHeight}px`;
                    }
                }
            });
        });

        document.addEventListener('click', () => {
            if (isMobileView()) {
                submenus.forEach((submenu) => submenu.style.height = '0');
                productLists.forEach((productList) => productList.style.maxHeight = '0');
                buttons.forEach((button) => button.classList.remove('active'));
                toggleButtons.forEach((toggleButton) => toggleButton.classList.remove('active'));
            }
        });
    };

    initDropdown();
</script>